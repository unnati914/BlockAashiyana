var city_states = Object();

//North America

city_states['Virginia'] = '|Fairfax|Richmond';
city_states['North Carolina'] = '|Charlotte|Raleigh|Durham|';
city_states['New York'] = '|Buffalo|Manhattan|Middletown|Niagara Falls|Rochester|';
city_states['California'] = '|Fremont|Irvine|Beverly Hills|';

